//          Assignment No:10 Print squares of numbers from 1 to 10

public class Square_of_numbers {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            int square = i * i;
            System.out.print(square + " ");
        }
    }
}
