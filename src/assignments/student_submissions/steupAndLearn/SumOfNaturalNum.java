                                    /* Calculate sum of first 10 natural numbers */

package assignments.student_submissions.steupAndLearn;

public class SumOfNaturalNum {

    public static void main(String[] args) {
         int num = 0;
        for (int i=1; i<=10; i++)
        {
            num = num + i;
        }
        System.out.println(num);
    }
}
