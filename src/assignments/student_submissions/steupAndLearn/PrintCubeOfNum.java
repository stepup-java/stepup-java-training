                                  /* Print cubes of numbers from 1 to 10*/

package assignments.student_submissions.steupAndLearn;

public class PrintCubeOfNum {

    public static void main(String[] args) {

        for (int i=1; i<=10; i++)
        {
            System.out.println("Cube of " + i + " = " + (i*i*i));
        }
    }
}
