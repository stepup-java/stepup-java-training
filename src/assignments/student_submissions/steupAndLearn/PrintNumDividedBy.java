                             /*Print numbers divisible by 3 from 1 to 50*/

package assignments.student_submissions.steupAndLearn;

public class PrintNumDividedBy {

    public static void main(String[] args) {

        for (int i=1; i<=50; i++)
        {
            if (i % 3 == 0)
            {
                System.out.println(i);
            }
        }
    }
}
