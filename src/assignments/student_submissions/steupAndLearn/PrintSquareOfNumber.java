                                     /* Print squares of numbers from 1 to 10*/

package assignments.student_submissions.steupAndLearn;

public class PrintSquareOfNumber {

    public static void main(String[] args) {

        for (int i=1; i<=10; i++)
        {
            System.out.println("Square of " + i + " = " + (i*i));
        }
    }
}
