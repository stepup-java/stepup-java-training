                               /* Print even numbers from 1 to 20*/

package assignments.student_submissions.steupAndLearn;

public class PrintEvenNumber {
    public static void main(String[] args) {

        for (int i=1; i<=20; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }

}
