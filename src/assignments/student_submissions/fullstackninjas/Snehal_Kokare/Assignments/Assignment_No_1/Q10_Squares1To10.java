// Question 10: Print squares of numbers from 1 to 10 using a for loop
class Q10_Squares1To10 {
    public static void main(String[] args) {
        System.out.print("squares of numbers from 1 to 10 using a for loop:\n ");
        for (int i = 1; i <= 10; i++) {
            System.out.print((i * i) + " ");
        }
        System.out.println();
    }
}