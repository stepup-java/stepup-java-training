    // Question 1: Print numbers from 1 to 10 using a for loop


    public class Q1_Print1To10 {
        public static void main(String[] args) {
            System.out.print("Numbers from 1 to 10 are  : ");
            for (int numbers = 1; numbers <= 10; numbers++) {
                System.out.print(numbers + " ");
            }
            System.out.println();
        }
    }

