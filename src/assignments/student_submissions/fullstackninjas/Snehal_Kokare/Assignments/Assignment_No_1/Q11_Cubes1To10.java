// Question 11: Print cubes of numbers from 1 to 10 using a for loop
class Q11_Cubes1To10 {
    public static void main(String[] args) {
        System.out.print("cubes of numbers from 1 to 10 using a for loop\n: ");
        for (int i = 1; i <= 10; i++) {
            System.out.print((i * i * i) + " ");
        }
        System.out.println();
    }
}