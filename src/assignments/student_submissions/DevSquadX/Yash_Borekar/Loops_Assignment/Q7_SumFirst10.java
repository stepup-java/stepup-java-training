// Question 7: Calculate sum of first 10 natural numbers
public class Q7_SumFirst10 {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 1; i <= 10; i++) 
         sum += i;
        
        System.out.println("Sum = " + sum);
    }
}