package assignments.student_submissions.nilesh_mungase;

public class SquareOfNumersOneToTen {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i + " * " + i + " = " + i * i);
		}
	}

}
